#include <stdio.h>

int main() {
	//Declaramos una variable
	int miNumero;
	
	//la inicializamos
	miNumero = 10;
	
	//imprimir la varaible
	//formato %d -> decimal
	printf("Mi numero es: %d", miNumero);
	
	//1. declaramos e inicializamos la variable
	int miNumero2 = 20, miNumero3 = 30;
	
	//2. Imprimir el valor de las variables
	printf ("\nMi numero dos es: %d, Mi numero tres es: %d", miNumero2, miNumero3);
	
	return 0;
}

